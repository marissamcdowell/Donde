import UIKit

enum Rtype {
    case Mother
    case Father
    case Brother
    case Sister
    case Cousin
    case Friend
    case BestFriend
    case Boyfriend
    case Girlfriend
    case CoWorker
    case Other
}

struct Person: NSObject{
//Person is a structure, not a class, so
//place it wherever you see it fit
    let iCloudID: String
    var icon: UIImage
    var relationship: Rtype
    
    init(name: String, type : Rtype) {
        self.iCloudID = name
        self.relationship = Rtype
    }
}

struct Memo{
    let name: String
    var rec: AVAudio
    var relate: [Rtype]
}